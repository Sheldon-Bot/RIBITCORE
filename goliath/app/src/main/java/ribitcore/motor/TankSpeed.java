package ribitcore.motor;

/**
 * Represents a tank speed (movement of both motors in unison).
 */
public enum TankSpeed {

    /**
     * The forward tank speed.
     */
    FORWARDS,

    /**
     * The backwards tank speed.
     */
    BACKWARDS,

    /**
     * The left tank speed.
     */
    LEFT,

    /**
     * The right tank speed.
     */
    RIGHT
}
